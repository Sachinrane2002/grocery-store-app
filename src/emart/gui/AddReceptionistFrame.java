    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int result = ValidateInputs();
        if(result==0){
            JOptionPane.showMessageDialog(null,"pleace fill password more 4 characters!");
            return;
        }
        if(result==-1){
            JOptionPane.showMessageDialog(null,"pleace Enter userId filled!");
            return;
        }
        char[] pass = Pass.getPassword();
        char[] repass = Re_Pass.getPassword();
        String pStr = String.valueOf(pass);
        String ReStr = String.valueOf(repass);
        if(isPasswordMaching(pStr,ReStr)==false){
            JOptionPane.showMessageDialog(null,"Password Do Not Maching");
            return;
        }
        try{
            String empId = jcEmpId.getSelectedItem().toString();
            String empName = txtEmpName.getText().trim();
            String userId = txtUserId.getText().trim();
            UserPojo user = new UserPojo();
            user.setEmpid(empId);
            user.setUsername(empName);
            user.setUserid(userId);
            user.setPassword(pStr);
            user.setUsertype("Receptionist");
            boolean status = ReceptionistDAO.addReceptionist(user);
            if(status){
               JOptionPane.showMessageDialog(null,"Receptionist added Succefully!");
               clearText();
               loadReceptionistDeleails();
                return;
            }
            JOptionPane.showMessageDialog(null,"Receptionist Not Added!");
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "DB Error");
            ex.printStackTrace();
        }
        
        
    }//GEN-LAST:event_btnAddActionPerformed

