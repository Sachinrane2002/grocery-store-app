    private void loadReceptionistDeleails() {
        try{
            receptionistMap = ReceptionistDAO.getNonRegisterReceptionist();
            if(receptionistMap.isEmpty()){
                JOptionPane.showMessageDialog(null,"No un-register Receptionist prasent!");
                btnAdd.setEnabled(false);
                return;
            }
            btnAdd.setEnabled(true);
            Set<String> Keys=receptionistMap.keySet();
            Iterator<String> it = Keys.iterator();
            jcEmpId.removeAllItems();
            while(it.hasNext()){
                jcEmpId.addItem(it.next());
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "DB Error");
            ex.printStackTrace();
        }
    }

