    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        String userid = jcUserId.getSelectedItem().toString();
        try{
            boolean result = ReceptionistDAO.deleteReceptionist(userid);
            if(result){
              JOptionPane.showMessageDialog(null,"Receptionist deleted succefully!");
              jcUserId.removeItemAt(jcUserId.getSelectedIndex());
              if(jcUserId.getItemCount()==0){
                  btnRemove.setEnabled(false);
                  return;
              }
              
            }else{
                JOptionPane.showMessageDialog(null,"Receptionist Not deleted!");
            }
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error!");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

