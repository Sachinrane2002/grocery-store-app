    private void loadReceptonistUserId() {
        try{
            List<String> userIdList = ReceptionistDAO.getAllReceptionistUserId();
            if(userIdList.isEmpty()){
                JOptionPane.showMessageDialog(null,"No Receptionist Prasent!");
                return;
            }
            btnRemove.setEnabled(true);
            for(String Id : userIdList){
                jcUserId.addItem(Id);
            }
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
         }
    }
