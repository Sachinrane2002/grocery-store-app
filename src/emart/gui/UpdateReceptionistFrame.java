    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
      if(!validateInputs()){
          return;
      }
      try{
      String userid = jcEmpId.getSelectedItem().toString();
        boolean result = ReceptionistDAO.updatePassword(userid, Password);
        if(result){
            JOptionPane.showMessageDialog(null, "Password Update Successfully!");
            clearText();
            jcEmpId.setSelectedIndex(0);
                return;
        }
        JOptionPane.showMessageDialog(null, "Password Not Changess");
        clearText();
      }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
    }     
    }//GEN-LAST:event_btnUpdateActionPerformed

