    private void loadReceptionistDetails() {
        try{
            recpMap = ReceptionistDAO.getAllReceptionistId();
            if(recpMap.isEmpty()){
                JOptionPane.showMessageDialog(null, "No Receptionist Prasent!");
                return;
            }
            Set<String> set = recpMap.keySet();
            for(String Id:set){
                jcEmpId.addItem(Id);
            }
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
    }
        
    }
    private boolean validateInputs() {
        char [] ps = Pass.getPassword();
        char [] rPs = Re_Pass.getPassword();
        if(ps.length<4 || rPs.length<4){
            JOptionPane.showMessageDialog(null, "Password Must be Atleaste 4 Characters!");
            return false;
        }
        Password = String.valueOf(ps);
        String rePassword = String.valueOf(rPs);
        if(!Password.equals(rePassword)){
             JOptionPane.showMessageDialog(null, "Password Not Maching!");
            return false;
        }
        return true;
    }

    private void clearText() {
        Pass.setText("");
        Re_Pass.setText("");
        
    }

